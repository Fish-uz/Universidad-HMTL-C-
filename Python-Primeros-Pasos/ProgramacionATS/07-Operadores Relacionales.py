#Compara 2 valores entre si, esta comparacion produce un resultado de certeza o falsedad (Booleano)
'''
Operadores Relaciones
> Mayor que
< Menor que
>= Mayor o igual que
<= Menor o igual que
!= Diferente
== Igual
'''

#Ejemplos:
resultado = 10 < 20
print("Menor que")
print(resultado)

resultado = 10 > 20
print("Mayor que")
print(resultado)

resultado = 10 == 20
print("Igualdad")
print(resultado)

resultado = 10 != 20
print("Diferente")
print(resultado)

resultado = 10 <= 10
print("Menor o Igual")
print(resultado)

resultado = 10 >= 10
print("Mayor o Igual")
print(resultado)

